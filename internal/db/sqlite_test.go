package db

import (
	"context"
	"path/filepath"
	"testing"
)

func newTestDB(t *testing.T) Engine {
	t.Helper()
	ctx := context.Background()
	e, err := Open(ctx, filepath.Join(t.TempDir(), "t.db"))
	if err != nil {
		t.Fatalf("open: %v", err)
	}
	t.Cleanup(func() { e.Close() })
	mustExec(t, e, `CREATE TABLE users (id INTEGER PRIMARY KEY, name TEXT, email TEXT)`)
	mustExec(t, e, `INSERT INTO users (name, email) VALUES ('Ada','ada@x.io'), ('Linus', NULL)`)
	return e
}

func mustExec(t *testing.T, e Engine, q string) {
	t.Helper()
	if _, err := e.Exec(context.Background(), q); err != nil {
		t.Fatalf("exec %q: %v", q, err)
	}
}

func TestSQLiteReadOnlyEnforced(t *testing.T) {
	ctx := context.Background()
	path := filepath.Join(t.TempDir(), "ro.db")

	// Seed the file with a read-write connection.
	rw, err := Open(ctx, path)
	if err != nil {
		t.Fatal(err)
	}
	mustExec(t, rw, `CREATE TABLE t (id INTEGER PRIMARY KEY, n TEXT)`)
	mustExec(t, rw, `INSERT INTO t (n) VALUES ('a')`)
	rw.Close()

	ro, err := Open(ctx, path, ReadOnly(true))
	if err != nil {
		t.Fatal(err)
	}
	defer ro.Close()

	// Reads still work.
	rs, err := ro.Query(ctx, "SELECT id, n FROM t")
	if err != nil {
		t.Fatalf("read on read-only conn: %v", err)
	}
	if len(rs.Rows) != 1 {
		t.Fatalf("want 1 row, got %d", len(rs.Rows))
	}

	// Writes are refused by the engine itself, even though the app-layer guard is
	// bypassed here (Exec is called directly). This is the backstop for statements
	// the keyword classifier would misroute (EXPLAIN ANALYZE <DML>, write PRAGMAs…).
	if _, err := ro.Exec(ctx, `INSERT INTO t (n) VALUES ('b')`); err == nil {
		t.Fatal("INSERT succeeded on a read-only connection; want it refused")
	}
	if _, err := ro.Exec(ctx, `UPDATE t SET n = 'z'`); err == nil {
		t.Fatal("UPDATE succeeded on a read-only connection; want it refused")
	}
}

func TestSQLiteColumnsUnique(t *testing.T) {
	ctx := context.Background()
	e, err := Open(ctx, filepath.Join(t.TempDir(), "u.db"))
	if err != nil {
		t.Fatal(err)
	}
	defer e.Close()
	mustExec(t, e, `CREATE TABLE u (id INTEGER PRIMARY KEY, email TEXT UNIQUE, name TEXT)`)

	cols, err := e.Columns(ctx, TableRef{Name: "u"})
	if err != nil {
		t.Fatal(err)
	}
	got := map[string]Column{}
	for _, c := range cols {
		got[c.Name] = c
	}
	if c := got["id"]; !c.PrimaryKey || !c.Unique || !c.AutoGenerated {
		t.Errorf("id: %+v — want PK+Unique+AutoGenerated", c)
	}
	if c := got["email"]; !c.Unique || c.PrimaryKey {
		t.Errorf("email: %+v — want Unique, not PK", c)
	}
	if c := got["name"]; c.Unique {
		t.Errorf("name: %+v — want not Unique", c)
	}
}

func TestSQLiteIntrospectionAndQuery(t *testing.T) {
	e := newTestDB(t)
	ctx := context.Background()

	tbls, err := e.Tables(ctx)
	if err != nil {
		t.Fatal(err)
	}
	if len(tbls) != 1 || tbls[0].Name != "users" {
		t.Fatalf("tables: %+v", tbls)
	}

	ref := TableRef{Name: "users"}
	cols, err := e.Columns(ctx, ref)
	if err != nil {
		t.Fatal(err)
	}
	if len(cols) != 3 {
		t.Fatalf("want 3 cols, got %d", len(cols))
	}
	if !cols[0].PrimaryKey || !cols[0].AutoGenerated {
		t.Fatalf("id should be PK + auto-generated rowid alias: %+v", cols[0])
	}

	pk, err := e.PrimaryKey(ctx, ref)
	if err != nil {
		t.Fatal(err)
	}
	if len(pk) != 1 || pk[0] != "id" {
		t.Fatalf("pk: %v", pk)
	}

	rs, err := e.Query(ctx, "SELECT id, name, email FROM users ORDER BY id")
	if err != nil {
		t.Fatal(err)
	}
	if len(rs.Rows) != 2 {
		t.Fatalf("want 2 rows, got %d", len(rs.Rows))
	}
	if rs.Rows[1][2] != nil {
		t.Fatalf("Linus.email should scan as nil (NULL), got %v", rs.Rows[1][2])
	}
}
